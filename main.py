# Riassegnazione della variabile in Python
# Variabile Booleana

online = True
print (online)

# Riassegnazione del Valore
# Booleana → Booleana
online = True
online = False
print (online)

# Stringa → Lista
accesso: str = "negato"
accesso = [1,2,3]
print (accesso)

# Lista → Stringa
band = ["basso", "batteria", "chitarra"]
band: str = "rock band"
print (band)

# Tuple → Stringa
coordinate: tuple = (10,20)
print (coordinate)

coordinate: tuple = (10,20)
coordinate: str = "punto A"
print (coordinate)

